# This is the list that relates to the index provided to the 'zookeeper'.
animals = [camel, lion, deer, goose, bat, rabbit]

# Here is the only real code. A simple while loop, with 'True' to keep things live.
# This while loop will stay live and interactive until the user types 'exit'.
# This loop will ask the user to input the number of the 'camera' they want to view.
# It will show that camera, ask for the next camera number and wait for user input.
while True:
    user_input = input("Please enter the number of the habitat you would like to view: ")
    if user_input == 'exit':
        break
    while int(user_input) > 5:  # This avoids an error if the user inputs a number outside the index of the list
        user_input = input("Please enter a whole number from 0 to 5: ")
    print(animals[int(user_input)])  # This line will print the sections above containing the images.
print("See you later!")
